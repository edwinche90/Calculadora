/**
 * funcion sumar
 * @param {float} numero1 
 * @param {float} numero2 
 * @returns {float} resultado numero1 + numero2
 */
 function sumar(numero1,numero2){
    return numero1 + numero2;
}

/**
 * funcion restar
 * @param {float} numero1 
 * @param {float} numero2 
 * @returns  resultado numero1 - numero2
 */
function resta(numero1,numero2){
    return numero1-numero2;
}
/**
 * funcion multiplicacion
 * @param {float} numero1 
 * @param {float} numero2 
 * @returns  resultado numero1 * numero2
 */
 function multiplicacion(numero1,numero2){
    return numero1*numero2;
}
/**
 * funcion division
 * @param {float} numero1 
 * @param {float} numero2 
 * @returns  resultado numero1 / numero2
 */
 function division(numero1,numero2){
    return numero1 / numero2;
}

/**
 * funcion potencia
 * @param {float} numero1 
 * @param {float} numero2 
 * @returns resultado de la potencia en numero1 y numero2
 */
function potencia(numero1,numero2){
    return Math.pow(numero1,numero2);
}

